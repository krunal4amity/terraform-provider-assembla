package provider

import (
	"github.com/hashicorp/terraform/helper/schema"
	"github.com/hashicorp/terraform/terraform"
	"os"
	"testing"
)

var testAccProviders map[string]terraform.ResourceProvider
var testAccProvider *schema.Provider

func init() {
	testAccProvider = Provider().(*schema.Provider)
	testAccProviders = map[string]terraform.ResourceProvider{
		"assembla": testAccProvider,
	}
}

func TestProvider(t *testing.T) {
	if err := Provider().(*schema.Provider).InternalValidate(); err != nil {
		t.Fatalf("err: %s", err)
	}
}

func testAccPreCheck(t *testing.T) {
	if v := os.Getenv("ASSEMBLA_KEY"); v == "" {
		t.Fatal("ASSEMBLA_KEY must be set for acceptance tests")
	}
	if v := os.Getenv("ASSEMBLA_SECRET_KEY"); v == "" {
		t.Fatal("ASSEMBLA_SECRET_KEY must be set for acceptance tests")
	}

}